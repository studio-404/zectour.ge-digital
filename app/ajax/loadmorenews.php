<?php 
class loadmorenews
{
	public $out; 
	
	public function __construct()
	{
		
	}
	
	public function index(){
		require_once 'app/core/Config.php';
		require_once 'app/functions/request.php';
		require_once 'app/functions/calendar.php'; 

		$this->out = array(
			"Error" => array(
				"Code"=>1, 
				"Text"=>"მოხდა შეცდომა !",
				"Details"=>"!"
			)
		);

		$itemsnums = functions\request::index("POST","itemsnums");
		$Database = new Database("modules", array(
			"method"=>"selectModuleByTypeLoadmore",
			"type"=>"news",
			"from"=>$itemsnums,
			"num"=>4
		));
		$getter = $Database->getter();
		if($getter){
				$this->out = array(
					"Error" => array(
						"Code"=>0, 
						"Text"=>"",
						"Details"=>""
					),
					"Success"=>array(
						"Code"=>1, 
						"Text"=>"ოპერაცია შესრულდა წარმატებით !",
						"news"=>$getter,
						"Details"=>""
					)
				);
			}else{
				$this->out = array(
					"Error" => array(
						"Code"=>1, 
						"Text"=>"ოპერაციის შესრულებისას დაფიქსირდა შეცდომა !",
						"Details"=>""
					)
				);
			}

		return $this->out;
	}
}